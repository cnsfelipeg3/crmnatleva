/**
 * Search airports locally by IATA code, city name, airport name, or country.
 * Returns up to `limit` results.
 */
export function searchAirportsLocal(keyword: string, limit = 12): AirportEntry[] {
  if (!keyword || keyword.length < 1) return [];
  const q = keyword.toUpperCase();
  const qLower = keyword.toLowerCase();

  // Normalize accented characters for search
  const normalize = (s: string) =>
    s.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
  const qNorm = normalize(keyword);

  // Exact IATA match first
  const exactIata = AIRPORTS_DB.filter(a => a.iata === q);

  // Then prefix matches on IATA
  const prefixIata = AIRPORTS_DB.filter(a =>
    a.iata.startsWith(q) && a.iata !== q
  );

  // Then matches on city/name (normalized)
  const nameMatches = AIRPORTS_DB.filter(a => {
    if (a.iata === q || a.iata.startsWith(q)) return false;
    return (
      normalize(a.city).includes(qNorm) ||
      normalize(a.name).includes(qNorm) ||
      normalize(a.country).includes(qNorm)
    );
  });

  const combined = [...exactIata, ...prefixIata, ...nameMatches];
  // Deduplicate
  const seen = new Set<string>();
  const unique: AirportEntry[] = [];
  for (const a of combined) {
    if (!seen.has(a.iata)) {
      seen.add(a.iata);
      unique.push(a);
    }
    if (unique.length >= limit) break;
  }
  return unique;
}
